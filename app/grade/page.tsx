  const [baseWage, setBaseWage] = useState("40000");
  const [targetYear, setTargetYear] = useState(2024);
  const [result, setResult] = useState<{finalWage:number; steps:{year:number;wr:number;cr:number;cum:number}[]}|null>(null);
  const [error, setError] = useState("");

