function getTenureBracket(totalMonths: number): TenureBracket {
  if (totalMonths < 12)  return '1년미만';
  if (totalMonths < 36)  return '1년-2년';
  if (totalMonths < 60)  return '3년-4년';
  if (totalMonths < 120) return '5년-9년';
  if (totalMonths < 180) return '10년-14년';
  if (totalMonths < 240) return '15년-19년';
  if (totalMonths < 360) return '20년-29년';
  return '30년 이상';
}

function calcTenure(startDate: Date, endDate: Date): { years: number; months: number; totalMonths: number } {
  let years  = endDate.getFullYear() - startDate.getFullYear();
  let months = endDate.getMonth()    - startDate.getMonth();
  // DATEDIF 방식: endDate+1일 기준
  const adjustedEnd = new Date(endDate.getTime() + 86400000);
  years  = adjustedEnd.getFullYear() - startDate.getFullYear();
  months = adjustedEnd.getMonth()    - startDate.getMonth();
  if (months < 0) { years--; months += 12; }
  return { years, months, totalMonths: years * 12 + months };
}

