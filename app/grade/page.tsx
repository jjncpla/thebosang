const LS_KEY = "더보상_추가데이터_v1";

interface DynamicData {
  minimumWage:      Record<number, MinWageData>;
  compensation:     Record<number, CompData>;
  wageRate:         Record<number, number>;
  cpiRate:          Record<number, number>;
  constructionWage: Record<string, Record<string, number>>;
}

function loadDynamic(): DynamicData {
  if (typeof window === "undefined") return emptyDynamic();
  try {
    const raw = localStorage.getItem(LS_KEY);
    return raw ? JSON.parse(raw) : emptyDynamic();
  } catch { return emptyDynamic(); }
}

function saveDynamic(d: DynamicData) {
  localStorage.setItem(LS_KEY, JSON.stringify(d));
}

function emptyDynamic(): DynamicData {
  return { minimumWage:{}, compensation:{}, wageRate:{}, cpiRate:{}, constructionWage:{} };
}


